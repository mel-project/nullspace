pub fn size(i: usize) -> f32 {
    match i {
        0 => 0.0,
        1 => 4.0,
        2 => 8.0,
        3 => 12.0,
        4 => 16.0,
        5 => 20.0,
        6 => 24.0,
        7 => 28.0,
        8 => 32.0,
        9 => 36.0,
        10 => 40.0,
        12 => 48.0,
        16 => 64.0,
        20 => 80.0,
        24 => 96.0,
        32 => 128.0,
        40 => 160.0,
        48 => 192.0,
        56 => 224.0,
        64 => 256.0,
        72 => 288.0,
        80 => 320.0,
        96 => 384.0,
        _ => (i as f32) * 4.0, // Tailwind base: 1 = 0.25rem = 4px (at 16px root)
    }
}
